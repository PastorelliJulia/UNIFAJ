int main() {
	float base, altura, area;
	
	printf("Digite a base:  ");
	scanf("%f", &base);
	
	printf("Digite a altura:  ");
	scanf("%f", &altura);
	
	area = (base * altura ) /2;
	printf("O triangulo de base %f e altura %f tem area %f",
		base, altura, area);
	
	return 0;
}
